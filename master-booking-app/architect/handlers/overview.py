from aiogram import F, Router
from aiogram.types import CallbackQuery, InlineKeyboardButton, InlineKeyboardMarkup

router = Router()

OVERVIEW_TEXT = (
    "✨ <b>Как работает сервис</b>\n\n"
    "Вы создаёте собственного бота, а клиенты записываются к вам через него без лишних переписок и звонков. "
    "Сервис сделан так, чтобы вы могли запустить запись быстро и без технической подготовки.\n\n"
    "<b>1. Посмотрите демо</b>\n"
    "Откройте демо и посмотрите, как сервис выглядит глазами мастера и клиента. Так проще сразу понять, подходит ли вам такой формат записи.\n\n"
    "<b>2. Создайте своего бота</b>\n"
    "Подключите Telegram-бота через BotFather, отправьте токен сюда и получите готового помощника для записи клиентов.\n\n"
    "<b>3. Проверьте всё вживую</b>\n"
    "После создания у вас будет 2 часа, чтобы спокойно протестировать бота, открыть календарь, посмотреть кнопки и убедиться, что всё работает именно так, как вам нужно.\n\n"
    "<b>4. Настройте запись под себя</b>\n"
    "В календаре вы можете задать рабочие дни, исключения, интервалы или услуги, а также включить напоминания клиентам и недельные отчёты.\n\n"
    "<b>5. Заполните меню для клиентов</b>\n"
    "Добавьте прайс, ответы на частые вопросы, адрес, портфолио и собственные кнопки. Всё, что включено и заполнено, автоматически появится у клиентов в меню бота.\n\n"
    "<b>6. Принимайте записи без ручной рутины</b>\n"
    "Клиент проходит короткую регистрацию в Telegram, выбирает дату, время и услуги, а вы сразу получаете запись с контактами и ссылкой на его Telegram-профиль.\n\n"
    "<b>7. Продолжайте работу без перерыва</b>\n"
    "Если результат вас устраивает, оплатите подписку на 1, 6 или 12 месяцев. После оплаты бот продолжит работать без остановки."
)


@router.callback_query(F.data == "overview")
async def overview(callback: CallbackQuery):
    await callback.message.edit_text(
        OVERVIEW_TEXT,
        reply_markup=InlineKeyboardMarkup(inline_keyboard=[
            [InlineKeyboardButton(text="◀️ Назад", callback_data="back_to_menu")],
        ]),
        parse_mode="HTML",
    )
    await callback.answer()
